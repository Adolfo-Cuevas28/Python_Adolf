lista = [3 , 1 , 5 , 2 , 4, 3, 5]

print(lista.count(3)) #Busca el número de veces que se repite el valor 3
print(lista.index(4)) #Busca la primera posición donde salga el valor 4

print(lista)

lista.sort() #Ordena la lista en forma ascendente
print(lista)

lista.reverse() #Ordena la lista en forma descendente
print(lista)