import turtle

s = turtle.Screen() #Pantalla
t = turtle.Turtle() #Flecha

#Movimiento de la flecha
t.fd(100) #forward - avanza 100
t.rt(90) #right - mueve la dirección en 90
t.fd(100) #forward - avanza otros 100
t.lt(90) #left - mueve la dirección en 90
t.bk(100) #backward - mueve 100 hacia atrás

turtle.done()