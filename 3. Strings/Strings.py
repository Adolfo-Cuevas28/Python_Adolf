cadena="Ejemplo de 'cadena' de texto"
print(cadena)

# Escapar caracteres
cadena2="Otra \"cadena\" de texto"
print(cadena2)

# Salto de línea
cadena3="Tercera \ncadena de \ntexto"
print(cadena3)

# Tabulación
cadena4="Cuarta \tlínea de \ttexto "
print(cadena4)

# Elimar espacios
cadena5="Quinta \blínea de \btexto eo"
print(cadena5)

# Simbolo raro
cadena6="Sexta \flínea de \ftexto owo"
print(cadena6)

# elimina
cadena7="septima \rlínea de \rtexto owo"
print(cadena7)

# simbolo raro
cadena8="octava \vlínea de \vtexto owo"
print(cadena8)